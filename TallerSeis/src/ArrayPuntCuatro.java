
public class ArrayPuntCuatro {

	    public static void main(String[] args) {
	    	
	        byte[] valores = new byte[10];
	        
	        for (int i = 0; i < valores.length; i++) {
	            valores[i] = 1;
	        }
	        for (int i = 0; i < valores.length; i++) {
	            System.out.print(valores[i] + " ");
	        }
	    }
	
}
